/*Operadores Aritméticos em C#:
Para calcular adição, subtração, multiplicação e divisão em C#:*/

int x = 10;
int y = 3;

int soma = x + y;        // Adição
int subtracao = x - y;   // Subtração
int multiplicacao = x * y; // Multiplicação
int divisao = x / y;     // Divisão

Console.WriteLine($"Soma: {soma}, Subtração: {subtracao}, Multiplicação: {multiplicacao}, Divisão: {divisao}");
